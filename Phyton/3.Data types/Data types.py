#Data types

##1.Strings:letters
"Hello"
print("Hello")
print("2")
print("Hello"+" "+"you")
print("Hello"+" "+"2"+" "+"you")

##2.Integer:numbers
"2"
"3"
print(2)
print(2+3)
print(3-2)
print(2*3)
print(3/2)
print(3//2)
print(3**2)

##3.Float:decimals
"1.23456"
print(1.2345+2.3456)

##4.Booleans:True or False
True
False
"True=1"
print(True*1)
"False=0"
print(False*1)
print(True+True+False+False)

##6.Swiping data types
x='1' #a string
y='0' #a string
print(x+y)
print(int(x)+int(y)) #into a integer
print(float(x)+float(y)) #into float
print(bool(int(x))) #into boolean
print(str(bool(int(x)))+str(bool(int(x)))) #into a string


